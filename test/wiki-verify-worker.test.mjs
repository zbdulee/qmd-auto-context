test('trim_jsonl: 상한 초과 시 최근 절반만 유지', () => {
  const dir = mkdtempSync(join(tmpdir(), 'trim-'));
  const log = join(dir, 'verify-log.jsonl');
  const lines = Array.from({ length: 100 }, (_, i) => JSON.stringify({ i, pad: 'x'.repeat(50) })).join('\n') + '\n';
  writeFileSync(log, lines);
  try {
    execFileSync('python3', ['-c', `
import sys
sys.path.insert(0, 'core')
from pathlib import Path
import wiki_compile as w
w.trim_jsonl(Path(${JSON.stringify(log)}), max_bytes=1024)
`], { cwd: process.cwd(), encoding: 'utf8' });
    const rows = jsonl(log);
    assert.equal(rows.length, 50, '절반 유지');
    assert.equal(rows[0].i, 50, '최근 절반(뒤쪽)이 남음');
  } finally { rmSync(dir, { recursive: true, force: true }); }
});

test('end-to-end 피기백: compile worker 1회 실행으로 생성→검증→verified 승격까지', () => {
  // task 분기 mock: extraction 요청엔 candidates, verify 요청엔 pass verdict.
  const dual = join(mkdtempSync(join(tmpdir(), 'dual-')), 'dual.py');
  writeFileSync(dual, `#!/usr/bin/env python3
import json, sys
payload = json.loads(sys.stdin.read())
if payload.get('task') == 'verify':
    print(json.dumps({'verdict': 'pass', 'claims': [], 'reasons': []}))
else:
    print(json.dumps({'candidates': [{
      'title': 'E2E Decision',
      'summary': 'Durable decision: generated wiki pages cite source markdown.',
      'suggestedType': 'decision',
      'confidence': 'high',
      'targetPath': '.auto-context/wiki/decisions/e2e-decision.md'
    }]}))
`);
  const dir = mkdtempSync(join(tmpdir(), 'qwiki-e2e-'));
  mkdirSync(join(dir, '.auto-context', 'compile'), { recursive: true });
  mkdirSync(join(dir, '.auto-context', 'wiki'), { recursive: true });
  mkdirSync(join(dir, 'docs'), { recursive: true });
  writeFileSync(join(dir, 'docs', 'source.md'), '# Source\n\nDurable decision: generated wiki pages cite source markdown.\n');
  writeFileSync(join(dir, '.auto-context', 'settings.json'), JSON.stringify({
    indexing: true,
    collections: ['proj-docs', 'proj-wiki'],
    collectionPaths: { 'proj-docs': 'docs', 'proj-wiki': '.auto-context/wiki' },
    collectionRoles: { 'proj-docs': 'raw', 'proj-wiki': 'wiki' },
    wikiPath: '.auto-context/wiki',
    compile: {
      enabled: true,
      mode: 'auto-wiki',
      autoWrite: true,
      defaultStatus: 'generated',
      triggers: ['post_tool_source', 'manual'],
      semanticDedup: { enabled: false },
      extractor: { argv: ['python3', dual], timeout: 30 },
      verify: { enabled: true, timeout: 30 },
    },
  }));
  writeFileSync(join(dir, '.auto-context', 'compile', 'source-queue.jsonl'), JSON.stringify({
    ts: '2026-07-04T00:00:00Z',
    trigger: 'post_tool_source',
    engine: 'claude',
    cwd: dir,
    source: { kind: 'file', path: 'docs/source.md', collection: 'proj-docs' },
  }) + '\n');
  const dirtyQueue = join(mkdtempSync(join(tmpdir(), 'dirty-e2e-')), 'queue');
  try {
    execFileSync('python3', ['core/wiki_compile_worker.py', '--cwd', dir, '--flush-all'], {
      cwd: process.cwd(), encoding: 'utf8',
      env: { ...process.env, QMD_DIRTY_QUEUE: dirtyQueue },
    });
    const page = join(dir, '.auto-context', 'wiki', 'decisions', 'e2e-decision.md');
    assert.equal(existsSync(page), true);
    const text = readFileSync(page, 'utf8');
    assert.match(text, /^status: verified$/m, '한 번의 worker 실행에서 생성 직후 검증·승격');
    assert.match(text, /^verifiedBy: "?claude"?$/m);
    assert.equal(readFileSync(join(dir, '.auto-context', 'compile', 'verify-queue.jsonl'), 'utf8'), '');
  } finally { rmSync(dir, { recursive: true, force: true }); }
});
