import { test } from 'node:test';
import assert from 'node:assert/strict';
import { execFileSync } from 'node:child_process';
import { existsSync, mkdtempSync, mkdirSync, readFileSync, writeFileSync, rmSync } from 'node:fs';
import { tmpdir } from 'node:os';
import { join } from 'node:path';

const CARD_REL = '.auto-context/wiki/concepts/test-card.md';
const HASH = 'abc123';

function cardText(status = 'generated', extraFm = '') {
  return `---
title: "Test Card"
status: ${status}
createdBy: qmd-auto-context
reviewed: false${extraFm}
---

> Auto-generated by qmd-auto-context from conversation/work context. Review, edit, or delete if wrong.

<!-- qmd:auto:start id="main" sourceHash="${HASH}" -->
## Summary
Durable claim: the source documents cite markdown.
<!-- qmd:auto:end -->
`;
}

function setupProject({ verify = {}, extractorArgv = [], cardStatus = 'generated', jobOverrides = {}, withSource = true } = {}) {
  const dir = mkdtempSync(join(tmpdir(), 'qwiki-verify-'));
  mkdirSync(join(dir, '.auto-context', 'compile'), { recursive: true });
  mkdirSync(join(dir, '.auto-context', 'wiki', 'concepts'), { recursive: true });
  mkdirSync(join(dir, 'docs'), { recursive: true });
  if (withSource) {
    writeFileSync(join(dir, 'docs', 'source.md'), '# Source\n\nDurable claim: the source documents cite markdown.\n');
  }
  writeFileSync(join(dir, '.auto-context', 'settings.json'), JSON.stringify({
    indexing: true,
    collections: ['proj-docs', 'proj-wiki'],
    collectionPaths: { 'proj-docs': 'docs', 'proj-wiki': '.auto-context/wiki' },
    collectionRoles: { 'proj-docs': 'raw', 'proj-wiki': 'wiki' },
    wikiPath: '.auto-context/wiki',
    compile: {
      enabled: true,
      mode: 'auto-wiki',
      autoWrite: true,
      defaultStatus: 'generated',
      triggers: ['post_tool_source', 'manual'],
      maxSourceChars: 12000,
      extractor: { argv: extractorArgv, timeout: 30 },
      verify: { enabled: true, timeout: 30, ...verify },
    },
  }));
  writeFileSync(join(dir, CARD_REL), cardText(cardStatus));
  writeFileSync(join(dir, '.auto-context', 'compile', 'verify-queue.jsonl'), JSON.stringify({
    ts: '2026-07-04T00:00:00Z',
    targetPath: CARD_REL,
    sources: [{ kind: 'file', path: 'docs/source.md', collection: 'proj-docs' }],
    sourceHash: HASH,
    engine: 'claude',
    trigger: 'post_tool_source',
    ...jobOverrides,
  }) + '\n');
  return dir;
}

function mockVerifier(verdictJson) {
  const script = join(mkdtempSync(join(tmpdir(), 'verifier-')), 'verify.py');
  writeFileSync(script, `#!/usr/bin/env python3
import json, sys
payload = json.loads(sys.stdin.read())
assert payload.get('task') == 'verify', 'verify payload expected'
assert 'content' in payload['card']
assert payload['sources'], 'sources must be loaded'
print(json.dumps(json.loads(${JSON.stringify(JSON.stringify(verdictJson))})))
`);
  return script;
}

function runVerifyWorker(project, env = {}) {
  return execFileSync('python3', ['core/wiki_verify_worker.py', '--cwd', project, '--json'], {
    cwd: process.cwd(),
    encoding: 'utf8',
    env: { ...process.env, ...env },
  });
}

function jsonl(path) {
  if (!existsSync(path)) return [];
  return readFileSync(path, 'utf8').trim().split('\n').filter(Boolean).map((line) => JSON.parse(line));
}

test('verify pass → status verified + verifiedBy/verifiedAt 패치 + 재인덱싱 enqueue', () => {
  const verifier = mockVerifier({ verdict: 'pass', claims: [{ claim: 'c', supported: true, quote: 'q', sourcePath: 'docs/source.md' }], reasons: [] });
  const project = setupProject({ extractorArgv: ['python3', verifier] });
  const dirtyQueue = join(mkdtempSync(join(tmpdir(), 'dirty-vp-')), 'queue');
  try {
    const out = JSON.parse(runVerifyWorker(project, { QMD_DIRTY_QUEUE: dirtyQueue }));
    assert.equal(out.processed, 1);
    const text = readFileSync(join(project, CARD_REL), 'utf8');
    assert.match(text, /^status: verified$/m);
    assert.match(text, /^verifiedBy: "?claude"?$/m);
    assert.match(text, /^verifiedAt: /m);
    assert.match(text, /Durable claim/, '관리 블록 본문은 보존');
    assert.match(readFileSync(dirtyQueue, 'utf8'), /^proj-wiki\t/);
    const log = jsonl(join(project, '.auto-context', 'compile', 'verify-log.jsonl'));
    assert.equal(log[0].result, 'verified');
    assert.equal(readFileSync(join(project, '.auto-context', 'compile', 'verify-queue.jsonl'), 'utf8'), '');
  } finally { rmSync(project, { recursive: true, force: true }); }
});

test('verify fail + onFail 기본(delete) → 카드 삭제, tombstone 없음, 재인덱싱 enqueue', () => {
  const verifier = mockVerifier({ verdict: 'fail', claims: [{ claim: 'c', supported: false, quote: '', sourcePath: 'docs/source.md' }], reasons: ['source contradicts claim'] });
  const project = setupProject({ extractorArgv: ['python3', verifier] });
  const dirtyQueue = join(mkdtempSync(join(tmpdir(), 'dirty-vf-')), 'queue');
  try {
    runVerifyWorker(project, { QMD_DIRTY_QUEUE: dirtyQueue });
    assert.equal(existsSync(join(project, CARD_REL)), false, '반증된 카드는 즉시 삭제');
    const tombstones = join(project, '.auto-context', 'compile', 'tombstones.jsonl');
    assert.equal(jsonl(tombstones).length, 0, 'tombstone을 세우지 않아 소스 수정 후 재생성 가능');
    assert.match(readFileSync(dirtyQueue, 'utf8'), /^proj-wiki\t/);
    const log = jsonl(join(project, '.auto-context', 'compile', 'verify-log.jsonl'));
    assert.equal(log[0].result, 'deleted');
    assert.equal(log[0].reasons[0], 'source contradicts claim');
  } finally { rmSync(project, { recursive: true, force: true }); }
});

